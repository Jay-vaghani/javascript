// ************************************* JavaScript Switch Statement *************************************

let day = "tus";

switch (day) {
  case "mon":
    console.log("mon");
    break;
  case "tus":
    console.log("tus");
    break;
  case "wen":
    console.log("wen");
    break;
  case "thu":
    console.log("thu");
    break;
  case "fri":
    console.log("fri");
    break;
  case "sat":
    console.log("sat");
    break;
  case "sun":
    console.log("sun");
    break;

  default:
    console.log("not ok");
    break;
}
